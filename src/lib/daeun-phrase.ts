export function getDaeunPhrase(gan: string, ji: string): string {
  const ganInfo = GANJIBRANCH[gan]
  const jiInfo = GANJIBRANCH[ji]
  if (!ganInfo || !jiInfo) return '새로운 기운이 흐르는 시기입니다'
  return ELEMENT_PHRASE[ganInfo.element]?.[jiInfo.element] ?? '새로운 기운이 흐르는 시기입니다'
}
